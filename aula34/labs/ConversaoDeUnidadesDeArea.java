package com.loiane.cursojava.aula34.labs;

public class ConversaoDeUnidadesDeArea {

	public static double metrosParaPes(double n1) {
		return 10.76 * n1;
	}

	public static double pesParaCentimetros(double n1) {
		return 929 * n1;
	}

	public static double milhaParaAcres(double n1) {
		return 640 * n1;
	}

	public static double acresParaPes(double n1) {
		return 43560 * n1;
	}

}
