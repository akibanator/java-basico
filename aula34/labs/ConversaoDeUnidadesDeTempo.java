package com.loiane.cursojava.aula34.labs;

public class ConversaoDeUnidadesDeTempo {

	public static int minutosParaSegundos(int n1) {
		return 60 * n1;
	}

	public static int horasParaMinutos(int n1) {
		return 60 * n1;
	}

	public static int diasParaHoras(int n1) {
		return 24 * n1;
	}

	public static int semanasParaDias(int n1) {
		return 7 * n1;
	}

	public static int mesParaDias(int n1) {
		return 30 * n1;
	}

	public static double anosParaDias(int n1) {
		return 365.25 * n1;
	}
}
