package com.loiane.cursojava.aula34.labs;

public class ConversaoDeUnidadesDeVolume {

	public static double litrosParaCentimetros(double n1) {
		return 1000 * n1;
	}

	public static double metrosParaLitros(double n1) {
		return 1000 * n1;
	}

	public static double metrosParaPes(double n1) {
		return 35.32 * n1;
	}

	public static double galaoAmericanoParaPolegadas(double n1) {
		return 231 * n1;
	}

	public static double galaoAmericanoParaLitros(double n1) {
		return 3785 * n1;
	}
}
